#Color Model არის გზა რომელიც ეხმარება კომპიუტერს გაიგოს რა ფერი უნდა გამოჩნდეს ეკრანზე.

#Color model შეიქმნა იმისთვის, რომ ტექნიკურმა მოწყობილობებმა ეკრანმა, პრინტერმა და სხვ. შეძლონ ფერის ერთნაირი წარმოჩენა სხვადასხვა სისტემებსა და პროგრამებში.

#RGB = Red (წითელი), Green (მწვანე), Blue (ლურჯი) 3 ფერის შერევით ვიღებთ სხვა ფერს.

#HEX იგივეა რაც RGB, მაგრამ დაწერილია კოდურად, 16-ობით სისტემაში.